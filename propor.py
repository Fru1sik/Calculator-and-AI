print("Решаем пропорцию: a / b = c / x")
print("Введите значения:")

# a — это числитель левой дроби
a = float(input("a (числитель слева): "))

# b — это знаменатель левой дроби
b = float(input("b (знаменатель слева): "))

# c — это числитель правой дроби
c = float(input("c (числитель справа): "))

# Вычисляем x по формуле: x = (b * c) / a
x = (b * c) / a

# Выводим результат
print(f"\nОтвет:")
print(f"x = (b * c) / a = ({b} * {c}) / {a} = {x}")
